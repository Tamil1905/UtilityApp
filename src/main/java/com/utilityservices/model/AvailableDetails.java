package com.utilityservices.model;

public class AvailableDetails {
	
	private int id;
	private String name;
	private String mobile;
	private String specialist_in;
	private String status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getSpecialist_in() {
		return specialist_in;
	}
	public void setSpecialist_in(String specialist_in) {
		this.specialist_in = specialist_in;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public AvailableDetails() {
		super();
		
	}

	
}
